"""Общий (базовый) промпт оператора, применяемый ко всем ответам.

Хранится в WORKING_DIR/global_prompt.txt и читается ДИНАМИЧЕСКИ (без рестарта
сервера). Это базовая инструкция оператора (роль, правила ответа только по
контексту, обработка METAINFO и т.п.), общая для всех отделов и пользователей.

Приоритет при генерации (см. chat_routes): промпт отдела ЗАМЕНЯЕТ общий — то
есть общий применяется там, где у отдела нет собственного промпта.

Поведение хранилища:
- файла нет (первый запуск) → возвращается DEFAULT_GLOBAL_PROMPT ( shipped-дефолт);
- файл есть, но пустой → администратор намеренно очистил общий промпт (вернётся "");
- «Сбросить» в UI записывает DEFAULT_GLOBAL_PROMPT обратно.
"""

import os
from pathlib import Path

GLOBAL_PROMPT_FILENAME = "global_prompt.txt"
MAX_GLOBAL_PROMPT_LEN = 8000

# Базовый промпт оператора по умолчанию (редактируется администратором в WebUI:
# Настройки → Промпты → «Общий промпт»). «Рога и копыта» — это плейсхолдер
# названия компании, который администратор заменяет на своё.
DEFAULT_GLOBAL_PROMPT = (
    'Ты - оператор RAG системы компании "Рога и копыта".\n'
    "\n"
    "Отвечай только на основе предоставленного контекста, без додумываний и "
    "предположений.\n"
    "Если в предоставленном тебе контексте нет информации которая явно отвечает "
    "на вопрос пользователя, - скажи честно, что в документации этого нет.\n"
    "\n"
    'Если вопрос слишком общий, - "Как исправить ошибку", "Как войти", но в '
    "переданном контексте ты видишь много разных типов ошибок и инструкций по "
    "входу, проси пользователя уточнить свой запрос.\n"
    "\n"
    'В контексте ты можешь получить документы с блоком "METAINFO", это не нужно '
    "показывать пользователю, но это важная директива для тебя!\n"
    "\n"
    "Каждый ответ заканчивай фразой, был рад помочь!"
)


def _global_prompt_path() -> Path:
    working_dir = os.getenv("WORKING_DIR", "./rag_storage")
    return Path(working_dir) / GLOBAL_PROMPT_FILENAME


def get_global_prompt() -> str:
    """Текущий общий промпт. Файла нет → дефолт; пустой файл → '' (очищен)."""
    path = _global_prompt_path()
    if not path.is_file():
        return DEFAULT_GLOBAL_PROMPT
    try:
        return path.read_text(encoding="utf-8")
    except OSError:
        return DEFAULT_GLOBAL_PROMPT


def set_global_prompt(prompt: str) -> str:
    """Сохраняет общий промпт (атомарно). Пустая строка = очистить (общий не
    применяется). Возвращает сохранённое значение."""
    text = (prompt or "")[:MAX_GLOBAL_PROMPT_LEN]
    path = _global_prompt_path()
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp = path.with_name(path.name + ".tmp")
    with open(tmp, "w", encoding="utf-8") as f:
        f.write(text)
    os.replace(tmp, path)
    return text
