// ── Базовые типографские элементы ────────────────────────────────
function H({ children }: { children: React.ReactNode }) {
  return <h3 className="font-serif text-lg font-bold mt-6 mb-2 text-foreground first:mt-0">{children}</h3>
}
function Sub({ children }: { children: React.ReactNode }) {
  return <h4 className="font-semibold text-base mt-4 mb-1.5 text-foreground">{children}</h4>
}
function P({ children, className }: { children: React.ReactNode; className?: string }) {
  return <p className={cn('text-sm leading-relaxed text-foreground/90 mb-2', className)}>{children}</p>
}
function UL({ children }: { children: React.ReactNode }) {
  return <ul className="list-disc pl-5 space-y-1 mb-2">{children}</ul>
}
function OL({ children }: { children: React.ReactNode }) {
  return <ol className="list-decimal pl-5 space-y-1 mb-2">{children}</ol>
}
function LI({ children }: { children: React.ReactNode }) {
  return <li className="text-sm leading-relaxed text-foreground/90">{children}</li>
}
function Code({ children }: { children: React.ReactNode }) {
  return <code className="rounded bg-muted px-1.5 py-0.5 text-xs text-primary break-words">{children}</code>
}
function Pre({ children }: { children: React.ReactNode }) {
  return <pre className="overflow-auto rounded-md bg-muted p-3 text-xs mb-2">{children}</pre>
}
function Note({ children }: { children: React.ReactNode }) {
  return (
    <div className="my-3 rounded-md border-l-2 border-primary/60 bg-primary/5 px-3 py-2 text-sm text-foreground/90">
      {children}
    </div>
  )
}
function Title({ children }: { children: React.ReactNode }) {
  return <h2 className="font-serif text-2xl font-bold mb-3">{children}</h2>
}

